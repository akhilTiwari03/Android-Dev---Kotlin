fun main()
{
	var a: Int = 10
	var d: Double = 100.00
	var f: Float = 100.4f
	var l: Long = 1000000000
	var s: Short = 9
	var b: Byte = 1
	var c: Char = 'A'
	var string: String = """This is the string."""
	var S: Boolean = true
	var C: Boolean = false


	println("Int =" +a)
	println("Double =" +d)
	println("Float =" +f)
	println("Long =" +l)
	println("Short =" +s)
	println("Byte =" +b)
	println("Char =" +c)
	println("String =" + string)
	println("BooleanTrue =" + S)
	println("BooleanFalse =" +C)

}